package Gun14;

public class _03_forLoop {
    public static void main(String[] args) {

        // 0 dan 10 dahil olan sayilari ekrana yazdiriniz
        // 10 dan 0 dahl olan sayilari yazdiriniz. ( for dongusu ile )


        int i;

        for ( i=0; i<=10; i++)
            System.out.println("i= " + i );

        // diger soru

        System.out.println();

        for (i=10; i>=0; i--)
            System.out.println("i =" + i);

    }
}
