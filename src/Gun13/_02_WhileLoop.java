package Gun13;

public class _02_WhileLoop {
    public static void main(String[] args) {

        int sayac=0;
        while (sayac<5) // sayac 5 ten kucuk oldugu surece yap
        {
            System.out.println(sayac+1 + ".Merhaba");

            sayac++; // sayaci bir arttir

            // tekrarlanacak seyleri parantezin icine yazacaksin

        }
        System.out.println("program bitti");

    }
}
