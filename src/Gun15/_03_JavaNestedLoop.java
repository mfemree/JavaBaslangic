package Gun15;

public class _03_JavaNestedLoop {
    public static void main(String[] args) {

        // asagidaki goruntuyu veren programi yaziniz

        // *
        // **
        // ***
        // ****
        // *****


        for (int satir = 1; satir <=5; satir++) {

            for (int i = 1; i <= satir; i++)
                System.out.print("*");

            System.out.println();
        }
    }
}
